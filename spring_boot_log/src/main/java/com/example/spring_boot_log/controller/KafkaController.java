@RestController
@Log4j2
public class KafkaController {
    @Autowired
    private KafkaTemplate<String, String> kafkaTemplate;

    @RequestMapping("applog")
    public String logToKafka(@RequestParam("param")String logStr) {
        kafkaTemplate.send("ods_base_log", logStr);
        log.info(logStr);
        return "log_to_kafka";
    }
}
